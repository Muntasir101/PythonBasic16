
"""
parameter: are variables that we specify inside parentheses at the time of function definition.
argument: the values that are passed for the parameters when calling the function.
"""


def summation(a, b):
    result = a + b
    print(result)


def subtraction():
    c = 100
    d = 20
    result = c - d
    print(result)


summation(100, 200)
